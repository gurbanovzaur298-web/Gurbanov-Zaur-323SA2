colors = {
    "красный": (255, 0, 0),
    "зеленый": (0, 255, 0),
    "синий": (0, 0, 255),
    "желтый": (255, 255, 0),
    "белый": (255, 255, 255),
    "черный": (0, 0, 0)
}

r1, g1, b1 = colors["красный"]
r2, g2, b2 = colors["синий"]

mixed = ((r1 + r2) // 2, (g1 + g2) // 2, (b1 + b2) // 2)
print(f"Смешали красный и синий: {mixed}")

r, g, b = colors["белый"]
inverted = (255 - r, 255 - g, 255 - b)
print(f"Инвертировали белый: {inverted}")